package CodingExam_11Jan2026;

public class CodeExam009_DoWhile1to10 {
    public static void main(String[] args) {
        /*9. WAP to print numbers from 1 to 10 using a do-while loop.*/
        int i = 1;
        System.out.println("Printing from 1 to 10");
        do {
            System.out.print(i + " ");
            i++;
        } while (i <= 10);
    }
}
